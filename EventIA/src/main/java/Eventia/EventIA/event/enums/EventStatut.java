package Eventia.EventIA.event.enums;

public enum EventStatut {

    ACTIVE,      // L'événement est en cours de préparation ou peut être rejoint
    ARCHIVED,    // L'événement est terminé et archivé
    CANCELLED,   // L'événement a été annulé
    DRAFT        // L'événement est en brouillon, pas encore visible publiquement
}